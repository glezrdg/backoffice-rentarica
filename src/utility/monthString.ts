export const monthString = (number: number) => {
  if (number === 0) return "Enero";
  if (number === 1) return "Febrero";
  if (number === 2) return "Marzo";
  if (number === 3) return "abril";
  if (number === 4) return "mayo";
  if (number === 5) return "Junio";
  if (number === 6) return "Julio";
  if (number === 7) return "Agosto";
  if (number === 8) return "Septiembre";
  if (number === 9) return "Octubre";
  if (number === 10) return "Noviembre";
  if (number === 11) return "Diciembre";

  return null;
};
